########  Instalação e uso do Positron com controle de versão ########

#1. Instale o R-cran, o Positron e o Git
#2. Faça login no Positron com a conta do Github
#3. Crie um repositório no github e copie a url
#4. Crie um novo projeto com "new folder from git"
#5. Crie um arquivo Quarto e salve


#6. Configurar seu Nome de Usuário
#Este é o nome que aparecerá como autor dos commits.

git config --global user.name "Seu Nome Completo"

#7. Configurar seu E-mail
#Importante: Use exatamente o mesmo endereço de e-mail que você utiliza para fazer login na sua conta do GitHub. Isso é essencial para que o GitHub consiga vincular seus commits ao seu perfil.

git config --global user.email "seu.email@associadoaogithub.com"

#8. Na guia lateral "source control" clique em commit & push

#9. Atualize o repositório no github para conferir